package urender.api;

/**
 * Cubemap texture face assignment.
 */
public enum UTextureFaceAssignment {
	/**
	 * Positive X axis.
	 */
	X_POS,
	/**
	 * Negative X axis.
	 */
	X_NEG,
	/**
	 * Positive Y axis.
	 */
	Y_POS,
	/**
	 * Negative Y axis.
	 */
	Y_NEG,
	/**
	 * Positive Z axis.
	 */
	Z_POS,
	/**
	 * Negative Z axis.
	 */
	Z_NEG
}
